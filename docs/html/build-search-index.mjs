/*
 * build-search-index.mjs — generates search-index.js for the docs site.
 *
 * Scans every *.html page in this folder, extracts the <main> content split
 * into sections by heading (h1/h2/h3, keyed by their id), strips markup, and
 * writes a window.MT_DOCS array consumed by search.js.
 *
 * Re-run after editing the docs:   node build-search-index.mjs
 * (No dependencies — plain Node, ESM.)
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));

const NAMED = {
  '&amp;': '&', '&lt;': '<', '&gt;': '>', '&quot;': '"', '&#39;': "'", '&apos;': "'",
  '&mdash;': '—', '&ndash;': '–', '&rarr;': '→', '&larr;': '←',
  '&middot;': '·', '&hellip;': '…', '&copy;': '©', '&nbsp;': ' ',
  '&ge;': '≥', '&le;': '≤', '&times;': '×', '&deg;': '°',
};
function decode(s) {
  return s
    .replace(/&#(\d+);/g, (_, n) => String.fromCharCode(+n))
    .replace(/&[a-z]+;/gi, (m) => (m in NAMED ? NAMED[m] : ' '))
    .replace(/&amp;/g, '&');
}
function strip(html) {
  return decode(html.replace(/<[^>]+>/g, ' ')).replace(/\s+/g, ' ').trim();
}

const files = readdirSync(here).filter((f) => f.endsWith('.html')).sort();
const docs = [];

for (const f of files) {
  const html = readFileSync(join(here, f), 'utf8');

  // Page title: prefer <title>, trimmed of the doc-site suffix.
  const titleTag = html.match(/<title>([\s\S]*?)<\/title>/i);
  let title = titleTag ? strip(titleTag[1]).replace(/\s*[-–]\s*MultiTerminal.*$/i, '').trim() : f;

  // Index only the main content region (skip the sidebar nav/search box).
  const mainTag = html.match(/<main[^>]*>([\s\S]*?)<\/main>/i);
  let main = mainTag ? mainTag[1] : html;
  main = main
    .replace(/<button[^>]*class="[^"]*theme-toggle[^"]*"[^>]*>[\s\S]*?<\/button>/gi, ' ')
    .replace(/<script[\s\S]*?<\/script>/gi, ' ');

  // The page's real heading wins over the <title> tag when present.
  const h1 = main.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i);
  if (h1) title = strip(h1[1]) || title;

  // Split into sections at each h1/h2/h3.
  const headingRe = /<h([1-3])([^>]*)>([\s\S]*?)<\/h\1>/gi;
  const marks = [];
  let m;
  while ((m = headingRe.exec(main))) {
    const idMatch = m[2].match(/id="([^"]*)"/i);
    marks.push({ pos: m.index, end: headingRe.lastIndex, id: idMatch ? idMatch[1] : '', title: strip(m[3]) });
  }

  const sections = [];
  if (marks.length === 0) {
    sections.push({ id: '', t: title, x: strip(main).slice(0, 4000) });
  } else {
    for (let i = 0; i < marks.length; i++) {
      const start = marks[i].end;
      const stop = i + 1 < marks.length ? marks[i + 1].pos : main.length;
      sections.push({ id: marks[i].id, t: marks[i].title, x: strip(main.slice(start, stop)).slice(0, 1400) });
    }
  }

  docs.push({ u: f, t: title, s: sections });
}

const banner = '/* AUTO-GENERATED by build-search-index.mjs - do not edit by hand. Re-run: node build-search-index.mjs */\n';
writeFileSync(join(here, 'search-index.js'), banner + 'window.MT_DOCS = ' + JSON.stringify(docs) + ';\n', 'utf8');

const sectionCount = docs.reduce((a, d) => a + d.s.length, 0);
console.log(`Indexed ${docs.length} pages, ${sectionCount} sections -> search-index.js`);
